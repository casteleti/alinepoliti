<p>
  Minha formação como <strong>psicóloga</strong> começou na graduação em Psicologia pela <strong>UNAERP</strong>
  (Universidade de Ribeirão Preto), concluída em 2011. Foi ali que nasceu o interesse que guiaria toda a minha
  carreira: compreender como o modo de educar molda o <strong>desenvolvimento</strong> e o comportamento. Minha
  monografia, <strong>“Estilos parentais e comportamento de adolescentes”</strong>, sob a orientação da
  <strong>Profª. Dra. Teresinha Pavanello Godoy Costa</strong>, já apontava esse caminho.
</p>
<p>
  Investiguei a relação entre os <strong>estilos parentais</strong> e o <strong>comportamento na adolescência</strong>
  — uma fase de intensas mudanças físicas, psicológicas e sociais, marcada por descobertas, conflitos, ambivalências e
  pela busca da <strong>identidade</strong>. Quis entender como a forma de educar dos pais se reflete nesse momento tão
  delicado e decisivo da vida.
</p>
<p>
  A vivência da pesquisa foi um exercício de <strong>escuta da juventude</strong>. Acompanhei <strong>adolescentes</strong>
  de 15 anos e suas <strong>famílias</strong>, aplicando o <strong>Inventário de Estilos Parentais (IEP)</strong> e
  entrevistas que exploravam o cotidiano: rotinas, regras, amizades, escolhas e planos para o futuro. Ouvir cada
  história de perto deu sentido aos dados.
</p>
<p>
  O estudo reforçou uma ideia que carrego até hoje: os pais são os primeiros agentes socializadores dos filhos e
  educam, sobretudo, pelo <strong>exemplo</strong> — mais do que por sermões. E que a adolescência, quando atravessada
  com diálogo e limites afetuosos, é também uma oportunidade de fortalecer <strong>vínculos</strong> e prevenir
  sofrimentos.
</p>
<p>
  Como epígrafe, escolhi o poema <em>“Amor de Adolescente”</em>, de Vera Sousa Silva — um lembrete de que, por trás das
  transformações dessa idade, existe uma sensibilidade que merece <strong>acolhimento</strong>, não julgamento. Esse
  cuidado com o olhar humano me acompanha em cada atendimento.
</p>
<p>
  Esse trabalho foi a semente do que aprofundei depois no <a href="<?= url('/a-psicologa/pesquisas/mestrado') ?>">mestrado
  na USP</a>. Hoje, ele embasa minha atuação no <strong>atendimento de adolescentes</strong> e na <strong>orientação de
  pais</strong>, sempre na <strong>Terapia Cognitivo-Comportamental (TCC)</strong> — como <strong>psicóloga em
  Jaboticabal</strong>, presencial e online.
</p>

