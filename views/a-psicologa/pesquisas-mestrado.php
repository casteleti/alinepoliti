<p>
  Minha trajetória como <strong>psicóloga</strong> foi profundamente marcada pelo mestrado que realizei na
  <strong>Universidade de São Paulo (USP)</strong>, no Programa de Pós-Graduação em Psicologia da Faculdade de
  Filosofia, Ciências e Letras de Ribeirão Preto (<strong>FFCLRP</strong>), na área de Psicologia em Saúde e
  Desenvolvimento. Sob a orientação da <strong>Dra. Sônia Regina Loureiro</strong>, dediquei-me a estudar, com
  método e sensibilidade, um tema que conversa diretamente com a clínica: a relação entre as <strong>famílias</strong>,
  a <strong>depressão materna</strong> e o <strong>desenvolvimento infantil</strong>.
</p>
<p>
  Minha dissertação investigou as <strong>práticas educativas parentais</strong> de famílias nucleares intactas e o
  <strong>comportamento infantil</strong> de crianças que convivem com a depressão materna. Quis compreender como o
  modo de educar — o cuidado, os limites, a presença — se associa ao bem-estar emocional das crianças, especialmente
  quando a mãe enfrenta um quadro de <strong>depressão</strong>. É uma questão de <strong>saúde mental</strong> que,
  muitas vezes silenciosa, afeta profundamente o cotidiano de muitos lares.
</p>
<p>
  A vivência da pesquisa foi, acima de tudo, uma experiência de <strong>escuta</strong>. Acompanhei 42 famílias —
  mães, pais e <strong>crianças</strong> entre 8 e 10 anos — em encontros individuais e cuidadosos. Apliquei
  instrumentos reconhecidos cientificamente, como o <strong>Inventário de Estilos Parentais</strong>, o Questionário de
  Capacidades e Dificuldades (SDQ) e as Matrizes Progressivas de Raven, sempre com o compromisso ético de respeitar
  cada história. Estar perto dessas famílias me ensinou tanto quanto os dados.
</p>
<p>
  Teoricamente, ancorei o estudo na <strong>psicopatologia do desenvolvimento</strong>, que olha para os
  <strong>fatores de risco</strong> e de proteção ao longo da vida e para a <strong>resiliência</strong> diante das
  adversidades. Os resultados confirmaram algo que carrego até hoje: práticas educativas positivas, como a
  <strong>monitoria positiva</strong> e o <strong>vínculo afetivo</strong>, protegem; enquanto a sobrecarga emocional
  dos pais pode favorecer práticas negativas e dificuldades de comportamento. O <strong>envolvimento paterno</strong>
  também se mostrou essencial no cuidado com os filhos.
</p>
<p>
  Essa pesquisa não ficou no papel: tornou-se um modo de cuidar. Hoje, ela sustenta o meu trabalho de
  <strong>orientação de pais</strong>, o atendimento de crianças e <strong>adolescentes</strong> e o olhar atento para
  a <strong>parentalidade</strong> e a família como um todo — sempre dentro da <strong>Terapia
  Cognitivo-Comportamental (TCC)</strong>, uma <strong>psicoterapia baseada em evidências</strong>. É a mesma seriedade
  científica do mestrado, agora a serviço de cada pessoa.
</p>
<p>
  Acredito em uma <strong>psicologia clínica</strong> que une ciência e acolhimento. Como <strong>psicóloga em
  Jaboticabal</strong>, ofereço <strong>atendimento presencial</strong> e <strong>atendimento online</strong> para
  adolescentes, adultos e casais, em demandas como <strong>ansiedade</strong>, depressão e relações familiares. A
  pesquisa me lembra, todos os dias, de que por trás de cada sintoma há uma história — e que compreendê-la é o início
  do cuidado.
</p>

